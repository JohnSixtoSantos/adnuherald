class Edge
	@id = ""
	@source = ""
	@target = ""
	@size = 0
	@color = 0

	def id
		return @id
	end

	def id=(s)
		@id = s
	end

	def source
		return @source
	end

	def source=(s)
		@source = s
	end

	def target
		return @target
	end

	def target=(s)
		@target = s
	end

	def size
		return @size
	end

	def size=(s)
		@size = s
	end

	def color
		return @color
	end

	def color=(s)
		@color = s
	end
end