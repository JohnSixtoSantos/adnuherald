class Node
	@id = ""
	@label = ""
	@x = 0
	@y = 0
	@size = 0
	@color = 0

	def id
		return @id
	end

	def id=(s)
		@id = s
	end

	def label
		return @label
	end

	def label=(s)
		@label = s
	end

	def x
		return @x
	end

	def x=(s)
		@x = s
	end

	def y 
		return @y
	end

	def y=(s)
		@y = s
	end

	def size
		return @size
	end

	def size=(s)
		@size = s
	end

	def color
		return @color
	end

	def color=(s)
		@color = s
	end
end